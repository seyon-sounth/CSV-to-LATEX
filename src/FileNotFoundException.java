/**
 * Class FileNotFoundException
 */
public class FileNotFoundException extends Exception{
    /**
     * default constrcutor
     */
    public FileNotFoundException() {
    }

    /**
     * Parametrized constructor
     * @param message
     */
    public FileNotFoundException(String message) {
        super(message);
    }
}

